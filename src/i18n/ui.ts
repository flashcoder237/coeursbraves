// --- Helpers ---
export function getLangFromUrl(url: URL): Lang {
  const [, seg] = url.pathname.split("/");
  if (seg in languages) return seg as Lang;
  return defaultLang;
}

export function useTranslations(lang: Lang) {
  return function t(key: keyof (typeof ui)["fr"]): string {
    return ui[lang][key] ?? ui[defaultLang][key];
  };
}

// Préfixe un chemin racine ("/contact/") avec la locale courante ("/en/contact/").
export function localizePath(path: string, lang: Lang): string {
  if (lang === defaultLang) return path;
  return `/${lang}${path.startsWith("/") ? path : `/${path}`}`;
}

// Donne le chemin équivalent dans une autre langue (pour le sélecteur).
export function switchLangPath(url: URL, target: Lang): string {
  let path = url.pathname;
  // retire le préfixe de langue actuel s'il existe
  const [, seg, ...rest] = path.split("/");
  if (seg in languages) path = "/" + rest.join("/");
  if (!path.startsWith("/")) path = "/" + path;
  return target === defaultLang ? path : `/${target}${path}`;
}
